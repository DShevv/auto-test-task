import type { ProductT, SortOptionT } from "../api/types";

export function sortProducts(products: ProductT[], sort: SortOptionT): ProductT[] {
  return [...products].sort((a, b) => {
    switch (sort) {
      case 'price-asc':
        return a.price - b.price;
      case 'price-desc':
        return b.price - a.price;
      case 'title-asc':
        return a.title.localeCompare(b.title);
      case 'title-desc':
        return b.title.localeCompare(a.title);
      default:
        return 0;
    }
  });
}